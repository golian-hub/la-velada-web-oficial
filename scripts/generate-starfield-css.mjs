/**
 * Generates the starfield box-shadow CSS file.
 * Run: node scripts/generate-starfield-css.mjs
 * 
 * This pre-generates the deterministic star positions so they live
 * in a cacheable CSS file instead of inline HTML styles.
 */
import { writeFileSync } from 'node:fs'
import { resolve, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))

function generateStars(count, seed = 0) {
  const stars = []
  let s = seed + 1
  for (let i = 0; i < count; i++) {
    s = (s * 1664525 + 1013904223) & 0xffffffff
    const x = Math.abs(s % 2000)
    s = (s * 1664525 + 1013904223) & 0xffffffff
    const y = Math.abs(s % 2000)
    stars.push(`${x}px ${y}px #fff`)
  }
  return stars.join(',')
}

function generateColoredStars(count, seed, color) {
  const stars = []
  let s = seed + 1
  for (let i = 0; i < count; i++) {
    s = (s * 1664525 + 1013904223) & 0xffffffff
    const x = Math.abs(s % 2000)
    s = (s * 1664525 + 1013904223) & 0xffffffff
    const y = Math.abs(s % 2000)
    stars.push(`${x}px ${y}px ${color}`)
  }
  return stars.join(',')
}

const css = `/* Auto-generated starfield box-shadows — DO NOT EDIT MANUALLY */
/* Generated by scripts/generate-starfield-css.mjs */
#stars1 { box-shadow: ${generateStars(700, 1)}; }
#stars2 { box-shadow: ${generateStars(200, 2)}; }
#stars3 { box-shadow: ${generateStars(80, 3)}; }
#stars-gold { box-shadow: ${generateColoredStars(35, 42, '#c7a86b')}; }
#stars-blue { box-shadow: ${generateColoredStars(50, 77, '#6a8cc7')}; }
`

const outPath = resolve(__dirname, '../src/styles/starfield.css')
writeFileSync(outPath, css)
console.log(`✓ Generated ${outPath} (${css.length} bytes)`)
